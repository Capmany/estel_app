import time
import socket


class DDPConnection:
    def __init__(self, ip_address, port):
        self.sock = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
        self.ip_address = ip_address
        self.port = port

    def write(self, data):
        self.sock.sendto(bytes(data), (self.ip_address, self.port))
        return len(data)

